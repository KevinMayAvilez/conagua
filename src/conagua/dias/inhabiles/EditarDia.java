    public void llenarCampos() {

        try {
            String sql = "select * from dias_inhabiles where id =" + id;

            con.Conectar();
            ResultSet rs = con.Consulta(sql);

            while (rs.next()) {
                jb_nombreD.setText(rs.getString("nombre"));
                jb_date.setDate(rs.getDate("fecha"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditarDia.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

