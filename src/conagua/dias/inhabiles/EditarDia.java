    private void jb_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_guardarActionPerformed

        if (!jb_nombreD.getText().trim().isEmpty() && jb_date.getDate() != null) {
            try {
                date = jb_date.getDate();
                sqlDate = new Date(date.getTime());

                con.Conectar();
                String sql = "insert into dias_inhabiles where id !=" + id;
                ResultSet rs = con.Consulta(sql);

                if (!rs.next()) {
                    sql = "update dias_inhabiles set "
                            + "nombre = ?,"
                            + "fecha = ?"
                            + "where id=" + id;
                    PreparedStatement ps = con.InsertPS(sql);
                    ps.setString(1, jb_nombreD.getText());
                    ps.setString(2, jb_date.getDateFormatString());

                }

                con.Cerrar();

                JOptionPane.showMessageDialog(null, "Se guardo correctamente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                ld.llenarTabla();

                //principal.setVisible(true);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(EditarDia.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltan datos por llenar.", "Â¡WARNING!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jb_guardarActionPerformed

